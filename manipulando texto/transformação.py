#Transformação

frase='curso em video python'

frase.replace('python','Android') # (REPLACE) substitui a palavra python por android

frase.upper()# (UPPER) deixa a frase totalmente maiuscula

frase.lower() # (LOWER) deixa a frase totalmente minuscula

frase.capitalize() # (capitalize) deixa a frase totalmente minuscula porem a primeira letra da linha maiuscula

frase.title() # (TITLE) onde tiver espaço, uma quebra de espaço ele deixara maiuscula a proxima letra 

palavra='   aprenda python    '

palavra.strip() # (STRIP) remove as quebras de espaços ao redor deixando mais organizado

palavra.rstrip()  # (RSTRIP) remove as quebras de espaços da direita

palavra.lstrip() # (LSTRIP) remove as quebras de espaços da esquerda
